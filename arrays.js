let frutas = ["pera", "manzana", "pina", "papaya"];

//Metodos para un array

frutas.length; // .length Dice la longitud de un array

frutas.indexOf("Uva")// Me dice la posicion de acuerdo al nombre del dato o objeto

frutas.push("Uva"); // .push Agrega un dato , objeto al array al final

frutas.pop(); // .pop elimina el ultimo dato del array

console.log(frutas[1]); // para ver la poscion de un dato en el array name[#]

frutas.unshift("banano"); // .unshift para agregar al comienzo del array

frutas.shift(); // Elimina el dato que este al principio del array


